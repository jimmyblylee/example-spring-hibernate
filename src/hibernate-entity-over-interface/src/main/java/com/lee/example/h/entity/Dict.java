package com.lee.example.h.entity;

public interface Dict {

    /**
     * @return the nature
     */
    String getNature();

    /**
     * @param nature
     *            the nature to set
     */
    void setNature(String nature);

    /**
     * @return the code
     */
    String getCode();

    /**
     * @param code
     *            the code to set
     */
    void setCode(String code);

    /**
     * @return the value
     */
    String getValue();

    /**
     * @param value
     *            the value to set
     */
    void setValue(String value);

}